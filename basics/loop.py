# Voorbeeld van een for-loop die door een reeks getallen loopt
for i in range(5):  # Van 0 tot 4 (5 is het exclusieve einde)
    print("Huidige waarde van i:", i)

# Voorbeeld van een while-loop die loopt zolang een voorwaarde waar is
j = 0
while j < 5:
    print("Huidige waarde van j:", j)
    j += 1  # Verhoog j met 1 in elke iteratie
